interface PersonalityTrait {
  label: string;
  key: string;
  value: number;
  description: string;
}

interface DialogueLine {
  id: string;
  condition: string;
  line: string;
  voiceType: string;
}

interface CombatBehavior {
  style: 'aggressive' | 'balanced' | 'defensive' | 'sniper' | 'stealth';
  retreatHealth: number;
  flankingEnabled: boolean;
  coverUsage: 'always' | 'sometimes' | 'never';
  meleeRange: number;
  grenadeUsage: boolean;
}

interface CompanionProfile {
  name: string;
  race: string;
  faction: string;
  voiceType: string;
  personality: PersonalityTrait[];
  combat: CombatBehavior;
  dialogue: DialogueLine[];
  affinityThresholds: { like: number; dislike: number; loathe: number; idolize: number };
}

