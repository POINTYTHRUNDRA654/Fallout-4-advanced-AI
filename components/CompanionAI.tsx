const SliderRow: React.FC<{
  trait: PersonalityTrait;
  onChange: (key: string, val: number) => void;
}> = ({ trait, onChange }) => (
  <div className="group space-y-1">
    <div className="flex items-center justify-between">
      <span className="text-xs font-mono text-slate-300">{trait.label}</span>
      <span className="text-xs font-mono text-emerald-400 w-8 text-right">{trait.value}</span>
    </div>
    <input
      type="range" min={0} max={100} value={trait.value}
      onChange={e => onChange(trait.key, parseInt(e.target.value))}
      className="w-full h-1.5 rounded-full appearance-none cursor-pointer
                 bg-slate-700 accent-emerald-500"
    />
    <p className="text-[10px] text-slate-500 group-hover:text-slate-400 transition-colors">{trait.description}</p>
  </div>
);

const Section: React.FC<{ title: string; icon: React.ReactNode; children: React.ReactNode; defaultOpen?: boolean }> = ({
  title, icon, children, defaultOpen = true
}) => {
  const [open, setOpen] = useState(defaultOpen);
  return (
    <div className="border border-slate-700/50 rounded-lg overflow-hidden">
      <button
        onClick={() => setOpen(!open)}
        className="w-full flex items-center justify-between px-4 py-3
                   bg-slate-800/60 hover:bg-slate-800 transition-colors text-left"
      >
        <div className="flex items-center gap-2 text-sm font-mono text-emerald-400">
          {icon} {title}
        </div>
        {open ? <ChevronDown className="w-4 h-4 text-slate-500" /> : <ChevronRight className="w-4 h-4 text-slate-500" />}
      </button>
      {open && <div className="p-4 bg-slate-900/40 space-y-3">{children}</div>}
    </div>
  );
};

// ─────────────────────────────────────────────────────────────────────────────
