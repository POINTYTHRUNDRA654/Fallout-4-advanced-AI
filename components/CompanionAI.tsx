// ─────────────────────────────────────────────────────────────────────────────
// Papyrus Generator
// ─────────────────────────────────────────────────────────────────────────────

function generatePapyrusScript(profile: CompanionProfile): string {
  return `; ─────────────────────────────────────────────────────────────────────────
; ${profile.name} Companion AI Script — Generated by Mossy FO4 Advanced AI
; ─────────────────────────────────────────────────────────────────────────
Scriptname ${profile.name}CompanionScript extends Actor

; ── Personality Constants ──────────────────────────────────────────────────
${profile.personality.map(t => `float Property ${t.key.charAt(0).toUpperCase() + t.key.slice(1)}Base = ${t.value / 100.0} Auto`).join('\n')}

; ── Affinity Thresholds ────────────────────────────────────────────────────
int Property AffinityLike    = ${profile.affinityThresholds.like}    Auto
int Property AffinityDislike = ${profile.affinityThresholds.dislike} Auto
int Property AffinityLoathe  = ${profile.affinityThresholds.loathe}  Auto
int Property AffinityIdolize = ${profile.affinityThresholds.idolize} Auto

; ── Combat Properties ──────────────────────────────────────────────────────
float Property RetreatHealthPct = ${profile.combat.retreatHealth / 100.0} Auto
bool  Property FlankingEnabled  = ${profile.combat.flankingEnabled} Auto
bool  Property GrenadeUsage     = ${profile.combat.grenadeUsage}    Auto

; ── Events ─────────────────────────────────────────────────────────────────
Event OnInit()
  RegisterForRemoteEvent(Game.GetPlayer(), "OnPlayerLoadGame")
  RegisterForRemoteEvent(Game.GetPlayer(), "OnCombatStateChanged")
  InitializePersonality()
EndEvent

Function InitializePersonality()
  ; Apply personality weights to combat style
  ActorValue kAggression = Game.GetFormFromFile(0x000002E7, "Fallout4.esm") as ActorValue
  Self.SetValue(kAggression, AggressionBase * 100.0)
  Debug.Notification("[${profile.name}] AI initialized — Style: ${profile.combat.style}")
EndFunction

Event OnCombatStateChanged(int aeCombatState)
  If aeCombatState == 1 ; Entering combat
    HandleCombatEntry()
  ElseIf aeCombatState == 0 ; Leaving combat
    HandleCombatExit()
  EndIf
EndEvent

Function HandleCombatEntry()
  Actor player = Game.GetPlayer()
  ${profile.combat.flankingEnabled ? `; Flanking logic\n  If Self.GetDistance(player.GetCombatTarget()) > ${profile.combat.meleeRange}\n    Self.SetValue(Game.GetFormFromFile(0x000002E8, "Fallout4.esm") as ActorValue, ConfidenceBase * 100)\n  EndIf` : '; Flanking disabled'}
  ${profile.combat.grenadeUsage ? `; Enable grenade package\n  Self.EvaluatePackage()` : ''}
EndFunction

Function HandleCombatExit()
  Float hpPct = Self.GetValue(Game.GetFormFromFile(0x00000015, "Fallout4.esm") as ActorValue) / Self.GetBaseValue(Game.GetFormFromFile(0x00000015, "Fallout4.esm") as ActorValue)
  If hpPct < RetreatHealthPct
    Debug.Notification("[${profile.name}] Low health — seeking cover")
  EndIf
EndFunction

; ── Affinity System ─────────────────────────────────────────────────────────
Int _currentAffinity = 0

Function ModAffinity(int delta)
  _currentAffinity += delta
  _currentAffinity = Math.Clamp(_currentAffinity, AffinityLoathe, AffinityIdolize) as Int
  EvaluateAffinityState()
EndFunction

Function EvaluateAffinityState()
  If _currentAffinity >= AffinityIdolize
    Debug.Notification("[${profile.name}] Idolizes player — unlocking perk")
  ElseIf _currentAffinity >= AffinityLike
    Debug.Notification("[${profile.name}] Likes player")
  ElseIf _currentAffinity <= AffinityLoathe
    Debug.Notification("[${profile.name}] Loathes player — may leave")
  ElseIf _currentAffinity <= AffinityDislike
    Debug.Notification("[${profile.name}] Dislikes player")
  EndIf
EndFunction
`;
}

